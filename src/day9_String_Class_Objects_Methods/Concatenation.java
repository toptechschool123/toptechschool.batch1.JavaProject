package day9_String_Class_Objects_Methods;

public class Concatenation {

	public static void main(String[] args) {

		/*
		 * String Concatenation Placing one String before the other String and combining
		 * them together is called string concatenation. The + operator used to add
		 * numbers and combine strings.
		 * 
		 */
		System.out.println(2 + 2);
		

		System.out.println("2" + "2");

		System.out.println("2" + 4);

		System.out.println("2" + "2" + 2 +2+ 3);

		System.out.println(2 + 2 + "2");

	}

}
